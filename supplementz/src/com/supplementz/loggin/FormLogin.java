    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
            String user = txtUsuario.getText();
        String pass = txtContrasenia.getText();

        String sql = "SELECT password, rol_id FROM Usuarios WHERE username = ?";

        try (Connection con = ConnectionDB.establishConnection();
             PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setString(1, user);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                String dbPass = rs.getString("password");  // contrase침a guardada en BD
                int rolId = rs.getInt("rol_id");

                if (pass.equals(dbPass)) {  // comparar texto plano directamente
                    if (rolId == 1) {
                        new Administrador().setVisible(true);
                        this.dispose();
                    } else if (rolId == 2) {
                        new Usuario().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "Rol no reconocido.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Contrase침a incorrecta.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El usuario no existe.");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error en la conexi칩n: " + ex.getMessage());
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

